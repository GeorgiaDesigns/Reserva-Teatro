    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Login validandoAcesso = new Login();
        conexao BD = new conexao("regulus","POO16172","POO16172","POO16172");
            if (!BD.conectaBD()) {
              JOptionPane.showMessageDialog(null,"Erro na conexão com o BD"); 
            }
            else 
            {
            try 
            {
                // pesquisando no BD se o usuário está cadastrado
                String senha = new String(jPasswordField1.getPassword());
                String idAcesso = jTextField2.getText();
                
                if (!validandoAcesso.pesquisaLogin(idAcesso, senha, BD.c))
                {
                    JOptionPane.showMessageDialog(null,"Acesso negado, voce não tem cadastro!");
                }
                else {
                    BD.desconectaBD();
                    TelaPrincipal TP = new TelaPrincipal();
                    
                    this.setVisible(false);
                    TP.setVisible(true);
                }
            }    
            catch (Exception ex) {
                Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            }
            } 
    }//GEN-LAST:event_jButton1ActionPerformed

