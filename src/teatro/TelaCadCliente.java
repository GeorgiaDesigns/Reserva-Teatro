    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Clientes clienteSQL = new Clientes();
        conexao BD = new conexao("regulus","POO16172","POO16172","POO16172");
        if (!BD.conectaBD()) 
        {
          JOptionPane.showMessageDialog(null,"Erro na conexão com o BD"); 
        }
        else 
        {
           try // tentando fazer o INSERT na tabela Cliente
           {
               double    CPF   = Double.parseDouble(jTextField1.getText());
               String nomeC    = jTextField2.getText();
               String IdadeN   = jSpinner3.getValue().toString()+"/"+jSpinner2.getValue().toString()+"/"+jSpinner1.getValue().toString();
               String senha    = new String(jPasswordField1.getPassword());
               
               try 
               {
                 Cliente objCliente = new Cliente(CPF,nomeC,IdadeN,senha);
                 clienteSQL.incluirCliente(objCliente,BD.c); 
                 JOptionPane.showMessageDialog(null,"Inclusão do cliente realizada!");
                 BD.desconectaBD();
               }
               catch (Exception ex) {
                 Logger.getLogger(TelaCadCliente.class.getName()).log(Level.SEVERE, null, ex);
                 JOptionPane.showMessageDialog(null,"ERRO na Inclusão do cliente!");
               }  
            } 
            catch (HeadlessException | NumberFormatException ex) {
              Logger.getLogger(TelaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(TelaCadCliente.class.getName()).log(Level.SEVERE, null, ex);
            }}
    }//GEN-LAST:event_jButton1ActionPerformed

